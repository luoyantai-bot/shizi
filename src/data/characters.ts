function buildCharacters(
  raw: [string, string, string][],
  level: 'low' | 'medium' | 'high',
  startId: number
): CharacterEntry[] {
  const seen = new Set<string>();
  const result: CharacterEntry[] = [];
  let order = 0;
  for (const [char, pinyin, word] of raw) {
    if (seen.has(char)) continue;
    seen.add(char);
    order++;
    result.push({
      id: `char_${String(startId + order).padStart(4, '0')}`,
      character: char,
      pinyin,
      wordExample: word,
      difficultyLevel: level,
      sortOrder: startId + order,
      imageUrl: `/images/char/${char}.png`,
    });
  }
  return result;
}

const lowChars = buildCharacters(lowRaw, 'low', 0);
const mediumChars = buildCharacters(mediumRaw, 'medium', 1000);
const highChars = buildCharacters(highRaw, 'high', 2000);

export const characters: CharacterEntry[] = [...lowChars, ...mediumChars, ...highChars];

export function getCharactersByLevel(level: 'low' | 'medium' | 'high'): CharacterEntry[] {
  return characters.filter(c => c.difficultyLevel === level);
}

export function getCharacterById(id: string): CharacterEntry | undefined {
  return characters.find(c => c.id === id);
}

